<b>A Királyok Koronájának legendája</b><br>
Sok-sok évszázaddal ezelőtt, a Sötétség Korszakában, amikor a világnak
szinte egyetlen pontját sem fedezték még fel, apró civilizációs fészkek
léteztek csupán, melyeknek saját kultúrájuk és fajuk volt. Az egyik ilyen
régió Kakhabad volt, egy sötét vidék a világ végén.
Bár számos hadvezér megkísérelte, Kakhabadot soha senkinek nem
sikerült kormányoznia. A Zanzunu Csúcsokon túli civilizáltabb területekről
elűzött gonosz teremtmények tömkelege szivárgott be fokozatosan
Kakhabadba, és ez a vidék csakhamar elnyerte a Világvégi Féregverem
elnevezést.
Amikor Chalanna, Femphrey nagy reformere megtalálta a Királyok Koro-
náját, a már felfedezett többi világrésen rend és civilizáció lett úrrá. A
Korona segítségével Chalanna lett e keleti földrész legnagyobb birodal-
mának uralkodója. Ennek a Varázskoronának misztikus hatalma volt,
birtokosát természetfölötti kormányzói és igazságtevői hatalommal ruházta
föl. Chalanna azonban nem hódítani akart, hanem Femphreyvel szövetsé-
ges, békés városállamokat kívánt létrehozni. Ezért aztán mesés Koronáját
bölcsen a szomszédos királyságok uralkodóinak adta át sorban, és annak
segítségével ezek a földek is egytől egyig felvirágoztak.
Fontos tervet dolgozott ki. Minden uralkodó négy évre kapja meg a Kirá-
lyok Koronáját, ezalatt rendet teremt országában, és csatlakozik
Femphrey szövetségeseinek egyre nagyobb táborához. A Korona segít-
ségével eddig Vaskövesd, Lendleland, Gallantaria és Brice királyságai
virágoztak fel, szinte egyik pillanatról a másikra. Ismeretlen jelenség volt e
vidékeken a háborúk minden fajtája.
Így került a Korona színpompás ceremónia közepette Analand királyának
kezébe, és ettől a naptól kezdve Analand óriási fejlődének indult. Senki
sem tudta, miként képes a Királyok Koronája ily magasra emelni egy
nemzetet. Némelyek ezt isteni erőknek tulajdonították, mások az ész
erejét emlegették. Egy dolog azonban nem volt kétséges: az eredmény.
Minden jól ment Analandban, mígnem leszállt a Fekete Hold éjszakája.
Elsőnek a király vette észre, hogy a Korona eltűnt. Azon a csillagtalan
éjszakán a Koronát Xameni Madáremberek ragadták fel, és Kakhabad
zord, törvényt nem ismerő vidékein át Mampang felé röpítették.
Baklandból jött a hír, hogy a Koronát Mampang fővarázslójának vitték el,
aki arra vágyott, hogy ő lehessen Kakhabad uralkodója. Bár Kakhabad
veszélyes vidék volt, ez a hír egymagában sokkal nagyobb veszélyt jelen-
tett a környező királyságokra. Kakhabadban nem léteztek törvények,
hadsereg sem volt, az országot belső bajai, viszályai teljesen lefoglalták. A
Királyok Koronájának segítségével Kakhabad a Femphreyi Szövetség
valamennyi tagjának veszedelmes ellenségévé válhatott volna.
A Királyok Koronájának eltűnése után Analand elvesztette mindazokat a
javakat és jótéteményeket, melyeket a Korona biztosított neki a két év
alatt. A jog, a rend és az erkölcs hanyatlásnak indult. A királyban egyre
inkább megrendült népe bizalma. A szomszédos területekről kíváncsian
lesték, mi történik a határaikon túli vidékkel. Még olyan hangok is hallat-
szottak, hogy támadják meg Analandot.
Már csak egyetlenegy remény maradt. Valakinek el kell mennie
Mampangba, és vissza kell szereznie a Királyok Koronáját, mivel a hadse-
reg soha nem lesz képes e feladat teljesítésére. Csak akkor szűnik meg
az Analandot sújtó átok, ha ez az illető épségben visszatér a Koronával.
Önként jelentkeztél erre a feladatra, küldetésed világos. El kell jutnod
Kakhabadba, Mampang erődjébe, és meg kell találnod a Koronát!</br>
